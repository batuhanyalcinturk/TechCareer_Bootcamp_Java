package com.graysan;

public class _06_Math {

    public static void main(String[] args) {
        System.out.println(Math.min(2,10000));
        System.out.println(Math.max(2,10000));
        System.out.println(Math.abs(-155)); // mutlak değer
        System.out.println(Math.sqrt(16));  // karekök
        System.out.println(Math.pow(2,5));  //
        System.out.println(Math.floor(8.9));
        System.out.println(Math.ceil(8.1));
        System.out.println(Math.round(5.4));
        System.out.println(Math.round(5.5));

    }
}
